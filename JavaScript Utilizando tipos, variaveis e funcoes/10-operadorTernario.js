const valor = 50;
const texto = valor < 50 ? "Valor insuficiente" : "Valor suficente";
console.log(texto); //Retorna suficiente

// operador ternário tem esse nome pois é o único em JavaScript que utiliza três operandos:
// 1- a condição, seguida do sinal ?
// 2- o código a ser executado se a condição for true, seguida de :
// 3- o código a ser executado se a condição for false.